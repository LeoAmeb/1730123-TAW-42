    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Product  $product
     * @return \Illuminate\Http\Response
     */
    public function destroy($product)
    {
        $producto = Product::findOrFail($product);
        $producto->delete();
    }

    public function list(){
        return Product::select('products.id', 'products.name','products.stock','products.price','microsites.name AS microsite', 'categories.name AS categorie')
      ->join('microsites','products.microsites_id','=','microsites.id')
      ->join('categories','products.categories_id','=','categories.id')
